unsigned char GetImageBit(const unsigned char *image,unsigned short x_size,unsigned short y_size,unsigned short x,unsigned short y)
{
	unsigned short tmp_byte,tmp_bit,tmp_y;
	unsigned short offset;
	tmp_byte = x/8;
	tmp_bit = x%8;
	tmp_y = y*(x_size /8);
	offset = tmp_y + tmp_byte;
	tmp_bit = 7 - tmp_bit;
	return (*(image + offset)&(1<<tmp_bit));	
}

void SetImageBit(unsigned char *image,unsigned short x_size,unsigned short y_size,unsigned short x,unsigned short y,unsigned char bit)
{
	unsigned short tmp_byte,tmp_bit,tmp_y;
	unsigned short offset;
	tmp_byte = x/8;
	tmp_bit = x%8;
	tmp_y = y*(x_size /8);
	offset = tmp_y + tmp_byte;	
	tmp_bit = 7 - tmp_bit;
	if(bit)
		{
			*(image + offset)|=(1<<tmp_bit);
		}
	else
		{
			*(image + offset)&=(~(1<<tmp_bit));
		}
}

void IMAGE_MixImage(const unsigned char *p_1,const unsigned char *p_2,unsigned short x_size,unsigned short y_size,unsigned short x_offset,unsigned short x_blank,unsigned char* out)
{
	unsigned short loopx,loopy;

	unsigned short tmp_iamge_1_x;//,tmp_iamge_1_y,tmp_iamge_2_x,tmp_iamge_2_y;

	//unsigned short g_x;
	if (x_offset > x_size + x_blank)
		{
			x_offset = x_size+x_blank;
		}
	for(loopy = 0 ; loopy < y_size ; loopy ++)
		{
			for(loopx = 0 ; loopx < x_size ; loopx++)
				{
					tmp_iamge_1_x = loopx + x_offset;
					if(tmp_iamge_1_x > (x_size + x_blank + x_size))// 2 image behind
						{
							SetImageBit(out,x_size,y_size,loopx,loopy,0);
						}
					if(tmp_iamge_1_x > (x_size + x_blank))// 2 image
						{
							SetImageBit(out,x_size,y_size,loopx,loopy,(GetImageBit(p_2,x_size,y_size,(loopx + x_offset  - x_size - x_blank),loopy)));
							continue;
						}
					if(tmp_iamge_1_x > (x_size))// blank
						{
							SetImageBit(out,x_size,y_size,loopx,loopy,0);
							continue;
						}	
					SetImageBit(out,x_size,y_size,loopx,loopy,(GetImageBit(p_1,x_size,y_size,(loopx  + x_offset),loopy)));
				}
		}
}

void GUI_ShowGrayImage(const unsigned char *p,unsigned short x,unsigned char y,unsigned short x_size,unsigned short y_size,Colour image_color,Colour back_color)
{
	unsigned int byte_step,bit_step;
	//unsigned int time_count = 0;
	Lcd_SetRegion(x,y,x+x_size-1,y+y_size-1);
	Lcd_WriteIndex(0x2C);
	//HAL_GPIO_WritePin(LCD_A0_GPIO_Port, LCD_A0_Pin, GPIO_PIN_SET);
	unsigned short loopx = (x_size * y_size)/8;
	
	for(byte_step=0;byte_step<loopx;byte_step++)
	for(bit_step=0;bit_step<8;bit_step++)
	{	
		if(*(p+byte_step) & (1<<(7-bit_step)))
			{
				LCD_WriteData_16Bit(image_color.Colour_Value);
			}
		else
			{
				LCD_WriteData_16Bit(back_color.Colour_Value);
			}
	} 
}

void GUI_DrawGrayImage(const unsigned char *p,unsigned short x,unsigned char y,unsigned short x_size,unsigned short y_size,Colour image_color,Colour back_color)
{
	unsigned int byte_step,bit_step,y_step,x_setp;
	//unsigned int time_count = 0;
	unsigned short loopx = (x_size)/8;
	byte_step=0;
	for(y_step = 0 ;y_step < y_size ; y_step++)
		{
			for(x_setp = 0 ; x_setp < loopx ; x_setp ++)
				{
					for(bit_step=0;bit_step<8;bit_step++)
						{	
							if(*(p+byte_step) & (1<<(7-bit_step)))
								{
									Gui_DrawPoint((x+x_setp*8+bit_step),(y+y_step), image_color.Colour_Value);
								}
							else
								{
								}
						}
					byte_step++;
				}
		}
}




